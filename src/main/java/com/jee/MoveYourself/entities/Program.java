package com.jee.MoveYourself.entities;

import jakarta.persistence.*;

import java.util.List;


@Entity
public class Program {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String name;

    @ManyToOne
    @JoinColumn(name = "user_id", nullable = false)
    private User user;

    @ManyToMany
    @JoinTable(
            name = "program_activity", // Name of the join table
            joinColumns = @JoinColumn(name = "program_id"), // Column for program ID
            inverseJoinColumns = @JoinColumn(name = "activity_id") // Column for activity ID
    )
    private List<Activity> activities;

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public List<Activity> getActivities() {
        return activities;
    }

    public void setActivities(List<Activity> activities) {
        this.activities = activities;
    }
}